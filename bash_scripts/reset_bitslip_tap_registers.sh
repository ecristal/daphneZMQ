#!/bin/bash

AFE0_TAP=0x8800000c
AFE1_TAP=0x88000010
AFE2_TAP=0x88000014
AFE3_TAP=0x88000018
AFE4_TAP=0x8800001c

AFE0_BITSLIP=0x88000020
AFE1_BITSLIP=0x88000024
AFE2_BITSLIP=0x88000028
AFE3_BITSLIP=0x8800002c
AFE4_BITSLIP=0x88000030

for AFE_ADDR in $AFE0_TAP $AFE0_BITSLIP $AFE1_TAP $AFE1_BITSLIP $AFE2_TAP $AFE2_BITSLIP $AFE3_TAP $AFE3_BITSLIP $AFE4_TAP $AFE4_BITSLIP
do

    # Check if the write was successful
    echo "Resetting TAP or BITSLIP to AFE at address $AFE_ADDR"
    sudo poke $AFE_ADDR 0x0 
done